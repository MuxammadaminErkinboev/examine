package model;

import model.enums.*;
import java.util.*;

public class Mentor {
    private UUID id;
    private String name;

    public Mentor(String name) {
        this.id = UUID.randomUUID();
        this.name = name;
    }

    public UUID getId() {
        return id;
    }

    public void setId(UUID id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
